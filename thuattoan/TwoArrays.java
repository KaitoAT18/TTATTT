package thuattoan;

public class TwoArrays {
    private int arr1[];
    private int arr2[];

    public TwoArrays (int[] arr1, int[] arr2) {
        this.arr1 = arr1;
        this.arr2 = arr2;
    }

    public int[] getArr1() {
        return arr1;
    }

    public int[] getArr2() {
        return arr2;
    }
}
